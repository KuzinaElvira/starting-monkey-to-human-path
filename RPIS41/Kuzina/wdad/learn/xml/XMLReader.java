package RPIS41.Kuzina.wdad.learn.xml;

import java.io.File;
import java.io.IOException;
import javax.xml.parsers.DocumentBuilder;
import javax.xml.parsers.DocumentBuilderFactory;
import javax.xml.parsers.ParserConfigurationException;
import org.w3c.dom.Document;
import org.xml.sax.SAXException;

/**
 *
 * @author Эльвира
 */
public class XMLReader {

    public static Document xmlReader(File file) throws ParserConfigurationException, SAXException, IOException {
        DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
        factory.setValidating(true);
        DocumentBuilder builder = factory.newDocumentBuilder();
        ErrorChecker errCheck = new ErrorChecker();
        builder.setErrorHandler(errCheck);
        Document document = builder.parse(file);        
        if(errCheck.isError()) System.exit(1);
        return document;
    }
}
